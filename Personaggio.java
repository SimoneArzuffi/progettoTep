public class Personaggio {
    String nome;
    String coloreCapelli;
    boolean occhiali;
    boolean cappello;
    boolean barba;
    boolean baffi;

    public Personaggio(String nome, String coloreCapelli, boolean occhiali, boolean cappello, boolean barba, boolean baffi) {
        this.nome = nome;
        this.coloreCapelli = coloreCapelli;
        this.occhiali = occhiali;
        this.cappello = cappello;
        this.barba = barba;
        this.baffi = baffi;
    }
}
